def find_unit_name(folder_name):
    for key, unit in UNIT_MAP.items():
        if key in folder_name:
            return unit
    return None


def read_carbon_outputs(root_dir, source_type):
    rows = []

    if not root_dir.exists():
        print(f"目录不存在: {root_dir}")
        return pd.DataFrame()

    for folder in root_dir.iterdir():

        if not folder.is_dir():
            continue

        unit_name = find_unit_name(folder.name)

        if unit_name is None:
            print(f"未匹配工艺单元，跳过: {folder.name}")
            continue

        hourly_file = folder / "hourly.parquet"

        if not hourly_file.exists():
            print(f"缺少 hourly.parquet，跳过: {folder.name}")
            continue

        df = pd.read_parquet(hourly_file)
        df.index = pd.to_datetime(df.index)

        if "unit_total_carbon_kg" not in df.columns:
            print(f"缺少 unit_total_carbon_kg，跳过: {folder.name}")
            continue

        stage_name = STAGE_MAP.get(unit_name)

        if stage_name is None:
            print(f"未匹配工艺段，跳过: {unit_name}")
            continue

        tmp = pd.DataFrame(index=df.index)
        tmp["carbon_kg"] = df["unit_total_carbon_kg"].fillna(0)
        tmp["process_stage"] = stage_name
        tmp["process_unit"] = unit_name
        tmp["source_type"] = source_type
        tmp["source_folder"] = folder.name

        rows.append(tmp.reset_index(names="time"))

    if len(rows) == 0:
        return pd.DataFrame()

    return pd.concat(rows, ignore_index=True)


def make_period_summary(all_df, freq):
    df = all_df.copy()
    df["time"] = pd.to_datetime(df["time"])

    source_summary = (
        df.set_index("time")
        .groupby(["process_stage", "process_unit", "source_type"])
        .resample(freq)["carbon_kg"]
        .sum()
        .reset_index()
        .rename(columns={"time": "period_start"})
    )

    source_pivot = (
        source_summary
        .pivot_table(
            index=["period_start", "process_stage", "process_unit"],
            columns="source_type",
            values="carbon_kg",
            aggfunc="sum",
            fill_value=0
        )
        .reset_index()
    )

    for col in ["电耗", "药耗"]:
        if col not in source_pivot.columns:
            source_pivot[col] = 0.0

    source_pivot = source_pivot.rename(columns={
        "电耗": "electric_carbon_kg",
        "药耗": "chemical_carbon_kg",
    })

    source_pivot["unit_total_carbon_kg"] = (
        source_pivot["electric_carbon_kg"]
        + source_pivot["chemical_carbon_kg"]
    )

    stage_total = (
        source_pivot
        .groupby(["period_start", "process_stage"])["unit_total_carbon_kg"]
        .sum()
        .reset_index()
        .rename(columns={"unit_total_carbon_kg": "stage_total_carbon_kg"})
    )

    plant_total = (
        stage_total
        .groupby("period_start")["stage_total_carbon_kg"]
        .sum()
        .reset_index()
        .rename(columns={"stage_total_carbon_kg": "plant_total_carbon_kg"})
    )

    unit_result = source_pivot.merge(
        stage_total,
        on=["period_start", "process_stage"],
        how="left"
    )

    unit_result = unit_result.merge(
        plant_total,
        on="period_start",
        how="left"
    )

    unit_result["unit_share_within_stage"] = (
        unit_result["unit_total_carbon_kg"]
        / unit_result["stage_total_carbon_kg"]
    )

    unit_result["unit_share_of_plant"] = (
        unit_result["unit_total_carbon_kg"]
        / unit_result["plant_total_carbon_kg"]
    )

    unit_result = unit_result.sort_values(
        ["period_start", "process_stage", "unit_total_carbon_kg"],
        ascending=[True, True, False]
    )

    stage_result = stage_total.merge(
        plant_total,
        on="period_start",
        how="left"
    )

    stage_result["stage_share_of_plant"] = (
        stage_result["stage_total_carbon_kg"]
        / stage_result["plant_total_carbon_kg"]
    )

    stage_result = stage_result.sort_values(
        ["period_start", "stage_total_carbon_kg"],
        ascending=[True, False]
    )

    return unit_result, stage_result


