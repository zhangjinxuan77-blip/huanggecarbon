        if "unit_total_carbon_kg" not in df.columns:
            print(f"缺少 unit_total_carbon_kg，跳过: {folder.name}")
            continue

        stage_name = STAGE_MAP.get(unit_name)

        if stage_name is None:
            print(f"未匹配工艺段，跳过: {unit_name}")
            continue

        tmp = pd.DataFrame(index=df.index)
        tmp["carbon_kg"] = df["unit_total_carbon_kg"].fillna(0)
        tmp["process_stage"] = stage_name
        tmp["process_unit"] = unit_name
        tmp["source_type"] = source_type
        tmp["source_folder"] = folder.name

        rows.append(tmp.reset_index(names="time"))

