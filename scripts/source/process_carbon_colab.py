def _safe_series(df: pd.DataFrame, col: str, default: float = 0.0) -> pd.Series:
    if col not in df.columns:
        raise KeyError(col)
    return pd.to_numeric(df[col], errors="coerce").fillna(default)


def runtime_from_col(df: pd.DataFrame, col: str, mode: str = "fraction") -> pd.Series:
    """
    mode = fraction:
        hourly聚合后的运行状态均值，0~1表示该小时运行比例。
    mode = minutes:
        每小时运行分钟数，转换为小时。
    mode = positive:
        只要值>0即认为该小时运行。
    """
    s = _safe_series(df, col)

    if mode == "minutes":
        return (s / 60.0).clip(lower=0, upper=1)

    if mode == "positive":
        return (s > 0).astype(float)

    return s.clip(lower=0, upper=1)


def calc_equipment(df: pd.DataFrame, eq_name: str, eq_config: dict, result_df: pd.DataFrame):
    status_col = eq_config["status"]
    runtime_mode = eq_config.get("runtime_mode", "fraction")

    runtime_h = runtime_from_col(df, status_col, runtime_mode)

    # 1) 实时功率
    if "power" in eq_config:
        power_kw = _safe_series(df, eq_config["power"])

    # 2) 固定功率 + 效率
    #    这里按“输入电耗功率 = 额定功率 / 效率”处理。
    #    如果你的功率已经是电输入功率，把 apply_efficiency 改成 False。
    elif "rated_power_kw" in eq_config:
        rated_power_kw = float(eq_config["rated_power_kw"])
        efficiency = float(eq_config.get("efficiency", 1.0))
        apply_efficiency = bool(eq_config.get("apply_efficiency", True))

        if apply_efficiency:
            power_kw_value = rated_power_kw / efficiency
        else:
            power_kw_value = rated_power_kw

        power_kw = pd.Series(power_kw_value, index=df.index)

    else:
        raise ValueError(f"{eq_name} 缺少 power 或 rated_power_kw 配置")

    energy_kwh = runtime_h * power_kw
    carbon_kg = energy_kwh * EMISSION_FACTOR

    result_df[f"{eq_name}_runtime_h"] = runtime_h
    result_df[f"{eq_name}_power_kW"] = power_kw
    result_df[f"{eq_name}_energy_kWh"] = energy_kwh
    result_df[f"{eq_name}_carbon_kg"] = carbon_kg


def calc_shared_power_unit(df: pd.DataFrame, equipment: dict, shared_power_col: str, result_df: pd.DataFrame):
    """
    用一个总功率测点给多台设备分摊。
    适用于：清水处理_送水泵。
    分摊逻辑：
        单台设备能耗 = 总功率 * 该设备运行小时 / 所有设备运行小时之和
    这样不会把同一个总功率重复计算多次。
    """
    total_power_kw = _safe_series(df, shared_power_col)

    runtime_map = {}
    for eq_name, eq_config in equipment.items():
        runtime_map[eq_name] = runtime_from_col(
            df,
            eq_config["status"],
            eq_config.get("runtime_mode", "minutes")
        )

    runtime_sum = pd.concat(runtime_map.values(), axis=1).sum(axis=1)
    runtime_sum_safe = runtime_sum.replace(0, pd.NA)

    for eq_name, runtime_h in runtime_map.items():
        share = (runtime_h / runtime_sum_safe).fillna(0)
        power_kw = total_power_kw * share
        energy_kwh = total_power_kw * share
        carbon_kg = energy_kwh * EMISSION_FACTOR

        result_df[f"{eq_name}_runtime_h"] = runtime_h
        result_df[f"{eq_name}_power_kW"] = power_kw
        result_df[f"{eq_name}_energy_kWh"] = energy_kwh
        result_df[f"{eq_name}_carbon_kg"] = carbon_kg


