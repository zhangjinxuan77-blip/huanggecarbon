def read_parquet(file_path):
    df = pd.read_parquet(file_path)
    df.index = pd.to_datetime(df.index)
    return df.sort_index()


def get_runtime(runtime_df, status_col, target_index):
    if runtime_df is None or status_col is None:
        return pd.Series(1.0, index=target_index)

    if status_col not in runtime_df.columns:
        print(f"缺失运行字段: {status_col}，按 1h 计算")
        return pd.Series(1.0, index=target_index)

    return (
        pd.to_numeric(runtime_df[status_col], errors="coerce")
        .reindex(target_index)
        .fillna(0)
        .clip(lower=0, upper=1)
    )


def clip_outlier_by_quantile(s, q=0.99):
    s = pd.to_numeric(s, errors="coerce").fillna(0)
    s = s.clip(lower=0)

    positive = s[s > 0]
    if len(positive) == 0:
        return s

    upper = positive.quantile(q)
    return s.clip(upper=upper)


