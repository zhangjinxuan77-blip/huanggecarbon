def add_unit_intensity(df):
    out = df.copy()
    out["光伏减排量_kgCO2"] = out["光伏减排量_tCO2"] * 1000
    out["单位水处理碳排强度_kgCO2e_per_m3"] = out["总碳排放_kgCO2e"] / out["水处理量_m3"]
    out["单位水处理光伏减排强度_kgCO2_per_m3"] = out["光伏减排量_kgCO2"] / out["水处理量_m3"]
    out["扣减光伏后单位水处理强度_kgCO2e_per_m3"] = (
        out["总碳排放_kgCO2e"] - out["光伏减排量_kgCO2"]
    ) / out["水处理量_m3"]
    out["单位水处理碳排强度_tCO2e_per_万m3"] = out["单位水处理碳排强度_kgCO2e_per_m3"] * 10
    out["单位水处理光伏减排量_tCO2_per_万m3"] = out["单位水处理光伏减排强度_kgCO2_per_m3"] * 10
    out["扣减光伏后单位水处理强度_tCO2e_per_万m3"] = (
        out["扣减光伏后单位水处理强度_kgCO2e_per_m3"] * 10
    )
    return out


