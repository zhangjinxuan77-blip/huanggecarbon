def filter_trend_window(df, window_type, input_path):
    date_col = find_date_column(df, input_path)

    if date_col is None:
        return df.tail(1), None

    df = df.copy()
    df["_parsed_date"] = pd.to_datetime(df[date_col], errors="coerce")
    valid_df = df[df["_parsed_date"].notna()].copy()

    if valid_df.empty:
        return df.tail(1).drop(columns=["_parsed_date"], errors="ignore"), date_col

    latest_time = valid_df["_parsed_date"].max()

    if window_type == "latest_24_hours":
        start_time = latest_time - pd.Timedelta(hours=24)
    elif window_type == "latest_7_days":
        start_time = latest_time - pd.Timedelta(days=7)
    elif window_type == "latest_30_days":
        start_time = latest_time - pd.Timedelta(days=30)
    elif window_type == "latest_12_months":
        start_time = latest_time - pd.DateOffset(months=12)
    else:
        result = valid_df.tail(1)
        return result.drop(columns=["_parsed_date"], errors="ignore"), date_col

    result = valid_df[
        (valid_df["_parsed_date"] > start_time)
        & (valid_df["_parsed_date"] <= latest_time)
    ]

    return (
        result
        .drop(columns=["_parsed_date"], errors="ignore")
        .sort_values(date_col),
        date_col
    )


def summarize_numeric(df, date_col=None):
    numeric_cols = df.select_dtypes(include="number").columns

    avg_keywords = [
        "share",
        "ratio",
        "rate",
        "percent",
        "efficiency",
        "factor",
        "distance",
    ]

    snapshot_keywords = [
        "plant_total",
        "stage_total",
        "total_carbon_all",
    ]

    result = {}

    for col in numeric_cols:
        col_lower = col.lower()

        if any(k in col_lower for k in avg_keywords):
            result[f"{col}_avg"] = df[col].mean()

        elif any(k in col_lower for k in snapshot_keywords):
            if date_col and date_col in df.columns:
                latest_idx = pd.to_datetime(
                    df[date_col],
                    errors="coerce"
                ).idxmax()
                result[f"{col}_latest"] = df.loc[latest_idx, col]
            else:
                result[f"{col}_max"] = df[col].max()

        else:
            result[f"{col}_sum"] = df[col].sum()

    return result


def build_summary_rows(trend_df, summary_label, source_file, date_col):
    base = {
        "summary_period": summary_label,
        "source_file": source_file,
        "row_count": len(trend_df),
    }

    if date_col and date_col in trend_df.columns and len(trend_df) > 0:
        parsed_time = pd.to_datetime(trend_df[date_col], errors="coerce")
        base["start_time"] = parsed_time.min()
        base["end_time"] = parsed_time.max()
    else:
        base["start_time"] = pd.NaT
        base["end_time"] = pd.NaT

    candidate_group_cols = [
        "process_stage",
        "process_unit",
        "source_type",
        "chemical_type",
        "source_folder",
        "unit_name",
        "scope",
        "section",
        "method",
    ]

    group_cols = [
        col for col in candidate_group_cols
        if col in trend_df.columns
    ]

    rows = []

    if group_cols and len(trend_df) > 0:
        grouped = trend_df.groupby(group_cols, dropna=False)

        for group_key, group_df in grouped:
            if not isinstance(group_key, tuple):
                group_key = (group_key,)

            row = base.copy()
            row["summary_level"] = "detail"

            for col, value in zip(group_cols, group_key):
                row[col] = value

            row["row_count"] = len(group_df)
            row.update(summarize_numeric(group_df, date_col=date_col))
            rows.append(row)

    total_row = base.copy()
    total_row["summary_level"] = "total"

    for col in group_cols:
        total_row[col] = "TOTAL"

    total_row.update(summarize_numeric(trend_df, date_col=date_col))
    rows.append(total_row)

    return rows


