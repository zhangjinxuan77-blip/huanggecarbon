def aggregate_period(df, freq):

    result = (
        df.set_index("time")
        .groupby("chemical_type")
        .resample(freq)
        .agg({
            "chemical_kg": "sum",
            "carbon_kg": "sum"
        })
        .reset_index()
    )

    result = result.rename(columns={
        "time": "period_start",
        "chemical_kg": "total_chemical_kg",
        "carbon_kg": "total_carbon_kg",
    })

    plant_total = (
        result
        .groupby("period_start")["total_carbon_kg"]
        .sum()
        .reset_index()
        .rename(columns={
            "total_carbon_kg": "plant_total_carbon_kg"
        })
    )

    result = result.merge(
        plant_total,
        on="period_start",
        how="left"
    )

    result["chemical_share_of_total"] = (
        result["total_carbon_kg"]
        / result["plant_total_carbon_kg"]
    )

    result = result.sort_values(
        ["period_start", "total_carbon_kg"],
        ascending=[True, False]
    )

    return result

