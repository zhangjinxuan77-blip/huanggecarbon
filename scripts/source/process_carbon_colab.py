OUTSIDE_KEYWORDS = [
    "原水取水",
    "清水处理",
]

INSIDE_KEYWORDS = [
    "预处理",
    "混凝沉淀",
    "过滤",
    "深度处理",
    "污泥处理",
]

# ============================================
# 判断工艺类别
# ============================================

def classify_stage(folder_name):

    for k in OUTSIDE_KEYWORDS:
        if k in folder_name:
            return "outside"

    for k in INSIDE_KEYWORDS:
        if k in folder_name:
            return "inside"

    return "unknown"

# ============================================
# 读取单个文件
# ============================================

def read_unit_file(file_path):

    df = pd.read_csv(file_path)

    # =========================
    # 时间列
    # =========================

    if "hour" in df.columns:
        time_col = "hour"
    elif "period_start" in df.columns:
        time_col = "period_start"
    else:
        raise KeyError(
            f"{file_path} 缺少时间列(hour / period_start)"
        )

    df[time_col] = pd.to_datetime(df[time_col])

    # =========================
    # 必须字段
    # =========================

    required_cols = [
        "unit_total_energy_kWh",
        "unit_total_carbon_kg",
    ]

    for col in required_cols:

        if col not in df.columns:
            raise KeyError(
                f"{file_path} 缺少字段: {col}"
            )

    # =========================
    # 输出
    # =========================

    result = pd.DataFrame({
        "period_start": df[time_col],

        "electricity_kwh": pd.to_numeric(
            df["unit_total_energy_kWh"],
            errors="coerce"
        ).fillna(0),

        "carbon_kg": pd.to_numeric(
            df["unit_total_carbon_kg"],
            errors="coerce"
        ).fillna(0),
    })

    return result
# ============================================
# 主循环
# ============================================

for period_name, filename in PERIOD_FILES.items():

    all_rows = []

    for folder in CARBON_DIR.iterdir():

        if not folder.is_dir():
            continue

        file_path = folder / filename

        if not file_path.exists():
            continue

        tmp = read_unit_file(file_path)

        tmp["folder_name"] = folder.name

        # 工艺类型
        tmp["boundary"] = tmp["folder_name"].apply(
            classify_stage
        )

        # 取水段
        tmp["is_intake"] = (
            tmp["folder_name"]
            .str.contains("原水取水")
        )

        # 送水段
        tmp["is_supply"] = (
            tmp["folder_name"]
            .str.contains("清水处理")
        )

        all_rows.append(tmp)

    all_df = pd.concat(
        all_rows,
        ignore_index=True
    )

    # ========================================
    # 全厂
    # ========================================

    plant_total = (
        all_df.groupby("period_start")
        .agg({
            "electricity_kwh": "sum",
            "carbon_kg": "sum"
        })
        .reset_index()
        .rename(columns={
            "electricity_kwh": "plant_total_electricity_kwh",
            "carbon_kg": "plant_total_carbon_kg",
        })
    )

    # ========================================
    # 厂外
    # ========================================

    outside_df = all_df[
        all_df["boundary"] == "outside"
    ]

    outside_total = (
        outside_df.groupby("period_start")
        .agg({
            "electricity_kwh": "sum",
            "carbon_kg": "sum"
        })
        .reset_index()
        .rename(columns={
            "electricity_kwh": "outside_plant_electricity_kwh",
            "carbon_kg": "outside_plant_carbon_kg",
        })
    )

    # ========================================
    # 厂内
    # ========================================

    inside_df = all_df[
        all_df["boundary"] == "inside"
    ]

    inside_total = (
        inside_df.groupby("period_start")
        .agg({
            "electricity_kwh": "sum",
            "carbon_kg": "sum"
        })
        .reset_index()
        .rename(columns={
            "electricity_kwh": "inside_plant_electricity_kwh",
            "carbon_kg": "inside_plant_carbon_kg",
        })
    )

    # ========================================
    # 取水段
    # ========================================

    intake_df = all_df[
        all_df["is_intake"]
    ]

    intake_total = (
        intake_df.groupby("period_start")
        .agg({
            "electricity_kwh": "sum",
            "carbon_kg": "sum"
        })
        .reset_index()
        .rename(columns={
            "electricity_kwh": "intake_electricity_kwh",
            "carbon_kg": "intake_carbon_kg",
        })
    )

    # ========================================
    # 送水段
    # ========================================

    supply_df = all_df[
        all_df["is_supply"]
    ]

    supply_total = (
        supply_df.groupby("period_start")
        .agg({
            "electricity_kwh": "sum",
            "carbon_kg": "sum"
        })
        .reset_index()
        .rename(columns={
            "electricity_kwh": "supply_electricity_kwh",
            "carbon_kg": "supply_carbon_kg",
        })
    )

    # ========================================
    # 合并
    # ========================================

    result = plant_total.copy()

    for tmp in [
        outside_total,
        inside_total,
        intake_total,
        supply_total,
    ]:

        result = result.merge(
            tmp,
            on="period_start",
            how="left"
        )

    result = result.fillna(0)

    # ========================================
    # 占比
    # ========================================

    result["outside_plant_share"] = (
        result["outside_plant_carbon_kg"]
        / result["plant_total_carbon_kg"]
    )

    result["inside_plant_share"] = (
        result["inside_plant_carbon_kg"]
        / result["plant_total_carbon_kg"]
    )

    result["intake_share"] = (
        result["intake_carbon_kg"]
        / result["plant_total_carbon_kg"]
    )

    result["supply_share"] = (
        result["supply_carbon_kg"]
        / result["plant_total_carbon_kg"]
    )

    result = result.fillna(0)

    # ========================================
    # 保存
    # ========================================

    result.to_csv(
        OUTPUT_DIR / filename,
        index=False
    )

