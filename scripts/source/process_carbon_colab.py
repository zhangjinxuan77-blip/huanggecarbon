# =========================
# 检查缺失测点
# =========================

print("\n开始检查缺失测点...")

for group_name, codes in groups.items():

    missing_codes = set(codes) - all_existing_codes

    if missing_codes:

        print(f"\n[{group_name}] 缺失测点:")

        for code in sorted(missing_codes):
            print(code)

print("\n测点检查完成")

# =========================
# 分块读取
# =========================

chunksize = 2_000_000

for chunk in pd.read_csv(
    input_file,
    skiprows=3,
    usecols=["_time", "_value", "code"],
    chunksize=chunksize
):

    # 只保留目标测点
    chunk = chunk[chunk["code"].isin(all_codes)]


    # 时间（适配 ISO8601 纳秒时间）
    chunk["_time"] = pd.to_datetime(
        chunk["_time"],
        format="ISO8601",
        errors="coerce"
    )

    # 数值
    chunk["_value"] = pd.to_numeric(
        chunk["_value"],
        errors="coerce"
    )

    # 删除异常时间/数值
    chunk = chunk.dropna(subset=["_time", "_value"])

    # 小时级
    chunk["hour"] = chunk["_time"].dt.floor("1H")



    # =========================
    # 每个group拆分
    # =========================

    for group_name, codes in groups.items():

        sub = chunk[chunk["code"].isin(codes)]

        if len(sub) == 0:
            continue

        # hourly 聚合
        agg = (
            sub.groupby(["hour", "code"])["_value"]
            .mean()
            .reset_index()
        )

        group_data[group_name].append(agg)

    print("处理一个chunk完成")

# =========================
# 最终保存
# =========================

for group_name, dfs in group_data.items():

    if len(dfs) == 0:
        continue

    df = pd.concat(dfs)

    # 再聚合一次（避免chunk边界重复）
    df = (
        df.groupby(["hour", "code"])["_value"]
        .mean()
        .reset_index()
    )

    # pivot
    pivot_df = df.pivot(
        index="hour",
        columns="code",
        values="_value"
    )

    # 排序
    pivot_df = pivot_df.sort_index()

    # 保存
    output_path = output_dir / f"{group_name}.parquet"

    pivot_df.to_parquet(output_path)

    print(f"已保存: {output_path}")

print("全部完成")

