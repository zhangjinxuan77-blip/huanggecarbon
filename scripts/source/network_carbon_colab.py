def rollup(df_in, key):
    g = (df_in.groupby([key,"zone","point"], as_index=False)
               .agg({
                    "kWh":"sum",
                    "CO2e_kg":"sum",
                    "flow_m3_h":"sum",
                    "pressure_m":"mean"
               }))
    g["SE_kWh_m3"] = np.where(g["flow_m3_h"]>0, g["kWh"]/g["flow_m3_h"], np.nan)
    g["I_kg_m3"]   = np.where(g["flow_m3_h"]>0, g["CO2e_kg"]/g["flow_m3_h"], np.nan)
    return g.rename(columns={key:"period"})

