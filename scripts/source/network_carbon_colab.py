# ====== 按行政区 × 管线长度区间 统计（CSV 分块版，仅长表TXT）======
import pandas as pd
import numpy as np
import os

# -------- 1) 参数区 --------
input_path  = "/content/drive/MyDrive/管网计算/管网坐标csv.csv"
length_col  = "长度(m)"     # 长度列名
district_col= "行政区"      # 行政区列名
output_dir  = "/content/drive/MyDrive/管网计算"  # 输出目录

# 长度分段（左闭右开）
bins   = [0, 1, 10, 50, 100, np.inf]
labels = ["0-1", "1-10", "10-50", "50-100", ">100"]

chunksize = 2_000_000  # 分块大小

# -------- 2) 分块统计 --------
total_agg = None

def process_chunk(chunk):
    global total_agg
    chunk.columns = chunk.columns.astype(str).str.strip()
    df = chunk[[district_col, length_col]].copy()

    # 清洗与类型
    df = df.dropna(subset=[district_col, length_col])
    df[length_col] = pd.to_numeric(df[length_col], errors="coerce")
    df = df.dropna(subset=[length_col])

    # 分段
    df["长度区间"] = pd.cut(
        df[length_col], bins=bins, labels=labels,
        right=False, include_lowest=True
    )

    # 本块分组聚合
    agg = (
        df.groupby([district_col, "长度区间"], observed=True)
          .size().rename("管线数量").reset_index()
    )

    # 累加
    if total_agg is None:
        total_agg = agg
    else:
        total_agg = pd.concat([total_agg, agg], ignore_index=True)
        total_agg = (
            total_agg.groupby([district_col, "长度区间"], observed=True)["管线数量"]
                     .sum().reset_index()
        )

# -------- 3) 分块读取并处理 --------
read_cols = [district_col, length_col]
for chunk in pd.read_csv(
    input_path,
    chunksize=chunksize,
    dtype=str,
    low_memory=True,
    usecols=read_cols,
    encoding_errors="ignore"
):
    process_chunk(chunk)

# -------- 4) 导出仅长表 --------
if total_agg is None or total_agg.empty:
    raise ValueError("未解析到有效记录，请检查列名/空值/数据格式。")

# 可选：按行政区 & 长度区间顺序排序
total_agg["长度区间"] = pd.Categorical(total_agg["长度区间"], categories=labels, ordered=True)
total_agg = total_agg.sort_values([district_col, "长度区间"]).reset_index(drop=True)

os.makedirs(output_dir, exist_ok=True)
long_txt_path  = os.path.join(output_dir, "管线长度统计_长表.txt")
total_agg.to_csv(long_txt_path, sep="\t", index=False, encoding="utf-8-sig")

print("完成✅ 已保存：", long_txt_path)
print("\n预览（长表前10行）：")
print(total_agg.head(10))

