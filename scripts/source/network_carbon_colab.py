def pick_flow_col(df):
    keys = ["数值", "瞬时流量", "流量", "流量(m³/h)", "流量(m3/h)", "当前流量"]
    for k in keys:
        for c in df.columns:
            if k in c and pd.api.types.is_numeric_dtype(df[c]):
                return c
    numeric = [c for c in df.select_dtypes(include=["float","int"]).columns]
    bad = [c for c in numeric if any(k in c for k in ["编码","编号","序号","ID","id"])]
    good = [c for c in numeric if c not in bad]
    return good[0] if good else (numeric[0] if numeric else None)

def normalize_point_name(s: str) -> str:
    """统一监测点名称：去掉路径、扩展名、末尾流水号等"""
    s = str(s).strip()
    base = os.path.basename(s)
    base = os.path.splitext(base)[0]
    base = re.sub(r"_\d+$", "", base)   # 去掉 _1751875...
    for t in ["超声波"]:
        base = base.replace(t, "")
    return base.strip()

def clean_name_from_filename(fname):
    return normalize_point_name(fname)

