# ========= 读取（两行表头：第1行=站点，第2行=区域）并聚合到“区级·按小时·中位数” =========
def read_zone_pressure_v2(pressure_path, keep_zone_map=None):
    xl = pd.ExcelFile(pressure_path)
    frames, diags = [], []

    for sh in xl.sheet_names:
        df0 = xl.parse(sh, header=None)
        if df0.empty or df0.shape[1] < 2:
            continue

        # === 新逻辑：第1行=区域，第2行=站点 ===
        zone_row_idx = 0
        site_row_idx = 1

        # 找时间列所在行（第3行）
        data_start_row = 2
        time_col = df0.iloc[data_start_row, 0]

        # 解析区域 & 站点
        zone_row = df0.iloc[zone_row_idx].astype(str).replace({"nan":None}).ffill()
        site_row = df0.iloc[site_row_idx].astype(str)

        # 构造列名
        cols = ["time"]
        for j in range(1, df0.shape[1]):
            z = str(zone_row.iloc[j]).strip()
            s = str(site_row.iloc[j]).strip()
            cols.append(f"{z}|{s}")

        data = df0.iloc[data_start_row:].copy()
        data.columns = cols

        # 解析时间
        data["time"] = pd.to_datetime(data["time"], errors="coerce")
        data = data.dropna(subset=["time"])
        data["hour"] = data["time"].dt.floor("h")
        data = data.drop(columns=["time"])

        # 转数字
        value_cols = [c for c in data.columns if c != "hour"]
        for c in value_cols:
            data[c] = pd.to_numeric(data[c], errors="coerce")

        # 宽 → 长
        long = []
        for c in value_cols:
            if "|" in c:
                z, s = c.split("|", 1)
            else:
                z = c
            long.append(data[["hour", c]].rename(columns={c: "value"}).assign(zone=z))
        long_df = pd.concat(long, ignore_index=True)

        # 按“小时×区域”中位数
        zone_hour = (
            long_df.groupby(["hour","zone"])["value"]
            .median()
            .reset_index()
            .pivot(index="hour", columns="zone", values="value")
        )

        frames.append(zone_hour)
        diags.append({
            "sheet": sh,
            "mode": "zone-row-1-site-row-2",
            "zones_found": ",".join(map(str, zone_hour.columns)),
            "rows_hour": len(zone_hour)
        })

    if not frames:
        return pd.DataFrame(columns=["hour"]), pd.DataFrame(diags)

    p = pd.concat(frames).groupby(level=0).median().reset_index()

    if isinstance(keep_zone_map, dict):
        wanted = ["hour"] + [z for z in keep_zone_map if z in p.columns]
        p = p[wanted]

    return p, pd.DataFrame(diags)

# ========== 主流程 ==========
